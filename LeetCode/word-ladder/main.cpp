// This problem is simple, but it needs a lot of optimization on constant complexity
// Another ``Keng''!!!
class Solution {
public:
    int ladderLength(string start, string end, unordered_set<string> &set) {
        n = set.size() + 2;
        m = set.begin()->length();
        list.push_back(start);
        mapping[start] = 0;
        int k = 1;
        for (string s : set)
        {
            list.push_back(s);
            mapping[s] = k++;
        }
        list.push_back(end);
        mapping[end] = k;
        
        int q[10000];
        vector<int> depth;
        for (int i = 0; i < n; i++)
        {
            depth.push_back(0);
        }
        depth[0] = 1;
        q[0] = 0;
        int qs = 0;
        int qe = 1;
        while (qs < qe)
        {
            auto t = q[qs];
            qs++;
            for (int i = 0; i < m; i++)
            {
                for (char c = 'a'; c <= 'z'; c++)
                {
                    if (list[t][i] != c)
                    {
                        string s = list[t];
                        s[i] = c;
                        if (mapping.find(s) != mapping.end())
                        {
                            int j = mapping[s];
                            if (depth[j] == 0)
                            {
                                depth[j] = depth[t] + 1;
                                if (j == n - 1)
                                {
                                    return depth[j];
                                }
                                q[qe++] = j;
                            }
                        }
                    }
                }
            }
        }
        return depth[n - 1];
    }

private:
    vector<string> list;
    unordered_map<string, int> mapping;
    int n, m;
};

